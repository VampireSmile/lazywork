package com.faceRecognition.modelHelper;

public class NeedInfo {
	private String id;
	private String dept;
	private String name;
	private String role;
	private String times;
	private String shouldtimes;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTimes() {
		return times;
	}

	public void setTimes(String times) {
		this.times = times;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getShouldtimes() {
		return shouldtimes;
	}

	public void setShouldtimes(String shouldtimes) {
		this.shouldtimes = shouldtimes;
	}

}
